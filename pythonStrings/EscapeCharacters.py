# 1 -- \' -- Single Quote
txt = 'It\'s alright.'
print(txt)

# 2 -- \\ -- Backslash
txt1 = "This will insert one \\ (backslash)."
print(txt1)

# 3 -- \n -- New line
txt2 = "Hello\nWorld!"
print(txt2)

# 4 -- \r -- Carriage Return
txt3 = "Hello\rWorld!"
print(txt3)

# 5 -- \t -- Tab
txt4 = "Hello\tWorld!"
print(txt4)

# 6 -- \b -- Backspace
txt5 = "Hello \bWorld!"
print(txt5)

# 7 -- \ooo -- Octal value
txt6 = "\110\145\154\154\157"
print(txt6)

#8 -- \xhh -- Hex value
txt7 = "\x48\x65\x6c\x6c\x6f"
print(txt7)